import Blog from '../models/Blog.js';
import { slugifyProductTitle } from '../../src/utils/productUrls.js';
import { triggerSitemapUpdate } from '../routes/sitemap.js';

const generateSlug = async (baseTitle) => {
    let slug = slugifyProductTitle(baseTitle);
    let isUnique = false;
    let counter = 0;
    while (!isUnique) {
        const candidate = counter === 0 ? slug : `${slug}-${counter}`;
        const exists = await Blog.findOne({ slug: candidate });
        if (!exists) {
            slug = candidate;
            isUnique = true;
        } else {
            counter++;
        }
    }
    return slug;
};

export const autoGenerateBlogForDeal = async (deal) => {
    try {
        console.log(`[AUTO-BLOG] Generating blog for deal: ${deal.title}`);
        
        // 1. Generate Metadata
        const blogTitle = `Why You Should Buy ${deal.shortTitle || deal.title} in 2026 - Review & Deal`;
        const slug = await generateSlug(blogTitle);
        const seoTitle = `${deal.shortTitle || deal.title} Review & Best Price Analysis`;
        const seoDescription = `Looking for the best deal on ${deal.shortTitle || deal.title}? Read our in-depth review, pricing analysis, and find out why this is a must-buy at ${deal.discountPercent}% OFF.`;
        
        // 2. Build Content Blocks with HTML support
        const contentBlocks = [];
        
        // Intro Block
        contentBlocks.push({
            type: 'intro',
            text: `Looking for the ultimate deal on the ${deal.title}? You've found it. In this quick review, we break down why this product is worth your money and how you can grab it at an unbeatable price today.`
        });
        
        // Value Proposition
        contentBlocks.push({
            type: 'heading',
            text: `Unbeatable Value at ${deal.priceDisplay || deal.dealPrice}`
        });
        
        contentBlocks.push({
            type: 'text',
            text: `Right now, you can get the <strong>${deal.title}</strong> for just <strong>${deal.priceDisplay || 'an amazing price'}</strong> (down from ${deal.originalPriceDisplay || 'its MSRP'}). That is a massive <strong>${deal.discountPercent || 0}% discount</strong>! Whether you're upgrading or buying for the first time, this price drop makes it a total steal.`
        });
        
        // Pro Tip
        contentBlocks.push({
            type: 'tip',
            text: `This deal is currently active on ${deal.storeName || 'our partner stores'}. Stock tends to run out fast at this price point, so we recommend acting quickly before it returns to its normal MSRP.`
        });
        
        // Specifications / Highlights (HTML List)
        if (deal.highlights && deal.highlights.length > 0) {
            contentBlocks.push({
                type: 'heading',
                text: 'Key Features & Specifications'
            });
            const listHtml = `<ul class="list-disc pl-5 mt-2 space-y-1">` + 
                deal.highlights.map(h => `<li>${h}</li>`).join('') + 
                `</ul>`;
            contentBlocks.push({
                type: 'text',
                text: listHtml
            });
        }
        
        // Conclusion
        contentBlocks.push({
            type: 'conclusion',
            text: `If you're on the fence, don't be. The ${deal.title} offers premium quality at a budget-friendly price. Click the deal link below to claim this offer before it expires.`
        });

        // 3. Create the Blog Document
        const blog = new Blog({
            title: blogTitle,
            slug: slug,
            summary: `A quick breakdown of the ${deal.title} deal and why it's a great purchase right now.`,
            contentBlocks: contentBlocks,
            featuredImage: deal.image || deal.thumbnail || '',
            category: deal.category || 'Deals',
            seoTitle: seoTitle,
            seoDescription: seoDescription,
            seoKeywords: [deal.brand, deal.storeName, 'Deals', 'Offers'].filter(Boolean),
            focusKeyword: deal.shortTitle || deal.title,
            tags: [deal.brand || 'Tech', 'Deals', deal.storeName].filter(Boolean),
            author: 'DealHunter Team',
            authorAvatar: 'DH',
            readTime: '3 min read',
            status: 'published',
            autoGenerated: true,
            sourceType: 'automatic',
            publishedAt: new Date(),
            relatedDeals: [{
                dealId: deal._id,
                title: deal.title,
                image: deal.image || deal.thumbnail,
                productUrl: deal.productUrl,
                affiliateLink: deal.affiliateLink,
                store: deal.storeName,
                category: deal.category,
                dealPrice: deal.dealPrice,
                mrp: deal.mrp,
                discountPercent: deal.discountPercent
            }]
        });

        await blog.save();
        console.log(`[AUTO-BLOG] Successfully generated blog: ${blog.slug}`);
        
        // Trigger sitemap regeneration instantly
        triggerSitemapUpdate();
    } catch (error) {
        console.error(`[AUTO-BLOG] Error generating blog for deal:`, error);
    }
};
